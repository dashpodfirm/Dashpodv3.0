#include "main_include.h"
#include "ble_comm.h"

int main(void)
{
    //This API intialize all the required peripherals for Dashpod
    TW_Dashpod_init();

    printf("%s:Dashpod Started\n",__TIME__);
        
    //This API loops for the mode selection which will be from BLE central device (Mobile)
    TW_dashpod_start_loop();

    //code will not reach this stage untill Radar sensor is failed to activate or read 
    printf("Dashpod exited\n");
    
    //If program reaches this area it will reastart againg but need to disable the softdevice
    nrf_sdh_disable_request();

     //This API will reset the Dashpod
    NVIC_SystemReset();
}